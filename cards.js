function getRandomCard() {
    return tarotDeck[Math.floor(Math.random() * tarotDeck.length)];
}

function getRandomCards(count) {
    const shuffled = [...tarotDeck].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, count);
}

